async def test_run_text_labels_propagated_from_ocr() -> None:
    """Labels returned by the OCR extractor appear in the metadata."""
    labels = [_make_label("R1", cx=0.2, cy=0.3), _make_label("R2", cx=0.6, cy=0.3)]
    pipeline = _make_pipeline(labels=labels)
    result = await pipeline.run(_white_image())
    assert len(result.text_labels) == 2
    assert {lbl.text for lbl in result.text_labels} == {"R1", "R2"}


async def test_run_no_labels_gives_empty_list() -> None:
    """Empty OCR output produces an empty text_labels list."""
    pipeline = _make_pipeline(labels=[])
    result = await pipeline.run(_white_image())
    assert result.text_labels == []


