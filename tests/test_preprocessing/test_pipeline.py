async def test_run_title_block_is_populated() -> None:
    """Title block fields are extracted from in-region OCR labels."""
    labels = [
        _make_label("DWG: SCHM-001", cx=0.70, cy=0.82),
        _make_label("REV: B", cx=0.88, cy=0.82),
        _make_label("1:50", cx=0.70, cy=0.88),
    ]
    pipeline = _make_pipeline(labels=labels)
    result = await pipeline.run(_white_image())

    assert result.title_block is not None
    assert result.title_block.drawing_id == "SCHM-001"
    assert result.title_block.revision == "B"
    assert result.title_block.scale == "1:50"


async def test_run_title_block_none_when_no_region_labels() -> None:
    """Title block bbox is still set even with no matching labels."""
    # All labels are in the body (outside title block region)
    labels = [_make_label("R1", cx=0.1, cy=0.1)]
    pipeline = _make_pipeline(labels=labels)
    result = await pipeline.run(_white_image())
    # TitleBlockExtractor always sets a bbox; fields default to empty strings
    assert result.title_block is not None
    assert result.title_block.drawing_id == ""


