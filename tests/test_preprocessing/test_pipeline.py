# ---------------------------------------------------------------------------
# Integration test — uses the synthetic fixture image from conftest.py
# ---------------------------------------------------------------------------


async def test_integration_full_pipeline(sample_electrical_image: Path) -> None:
    """Full pipeline integration: synthetic image → valid DiagramMetadata.

    The OCR extractor is mocked to return labels that match what real OCR
    would produce for the conftest fixture image (DWG: TEST-001, REV: A,
    SCALE: 1:1 drawn in the bottom-right title block area).
    """
    labels = [
        # Title block labels (inside bottom-right region)
        _make_label("DWG: TEST-001", cx=0.72, cy=0.84),
        _make_label("REV: A", cx=0.88, cy=0.84),
        _make_label("SCALE: 1:1", cx=0.72, cy=0.90),
        # Body labels (outside title block region — ignored by title extractor)
        _make_label("R1", cx=0.18, cy=0.29),
        _make_label("R2", cx=0.68, cy=0.29),
    ]

    pipeline = PreprocessingPipeline(
        ocr_extractor=_mock_extractor(labels),
        cv_pipeline=_StubCV(),  # type: ignore[arg-type]
    )
    result = await pipeline.run(sample_electrical_image)

    # Structural checks
    assert isinstance(result, DiagramMetadata)
    assert result.source_filename == "sample_electrical.png"
    assert result.format == "png"
    assert result.width_px == 800
    assert result.height_px == 600

    # All OCR labels surfaced
    assert len(result.text_labels) == 5

    # Title block extracted from in-region labels
    assert result.title_block is not None
    assert result.title_block.drawing_id == "TEST-001"
    assert result.title_block.revision == "A"
    assert result.title_block.scale == "1:1"
    assert result.title_block.bbox is not None
