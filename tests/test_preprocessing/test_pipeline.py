"""Tests for src/preprocessing/pipeline.py — PreprocessingPipeline.

Unit tests mock the OCR extractor and CV pipeline so no external services
or OpenCV are required.  The integration test at the bottom exercises the
full pipeline with a mocked OCR extractor against the synthetic fixture image
generated by conftest.py.
"""

