async def test_run_propagates_ocr_error() -> None:
    """Exceptions from the OCR extractor are re-raised unchanged."""
    extractor = MagicMock(spec=DocumentAIOCRExtractor)
    extractor.extract = AsyncMock(side_effect=RuntimeError("API unreachable"))
    pipeline = PreprocessingPipeline(ocr_extractor=extractor, cv_pipeline=_StubCV())  # type: ignore[arg-type]

    with pytest.raises(RuntimeError, match="API unreachable"):
        await pipeline.run(_white_image())


