class _StubCV:
    """CV pipeline stub — returns an empty CVResult without importing OpenCV."""

    def run(self, image: Image.Image, text_labels: object = None) -> CVResult:  # noqa: ARG002
        return CVResult()


def _make_label(
    text: str,
    cx: float = 0.75,
    cy: float = 0.85,
) -> TextLabel:
    """Create a TextLabel centred at (cx, cy) with a small bounding box."""
    hw, hh = 0.05, 0.02
    return TextLabel(
        text=text,
        bbox=BoundingBox(
            x_min=max(0.01, cx - hw),
            y_min=max(0.01, cy - hh),
            x_max=min(0.99, cx + hw),
            y_max=min(0.99, cy + hh),
        ),
        confidence=0.95,
    )


def _mock_extractor(labels: list[TextLabel] | None = None) -> DocumentAIOCRExtractor:
    """Return a DocumentAIOCRExtractor mock that resolves to *labels*."""
    extractor = MagicMock(spec=DocumentAIOCRExtractor)
    extractor.extract = AsyncMock(return_value=labels or [])
    return extractor


def _make_pipeline(
    labels: list[TextLabel] | None = None,
    cv: object | None = None,
) -> PreprocessingPipeline:
    return PreprocessingPipeline(
        ocr_extractor=_mock_extractor(labels),
        cv_pipeline=cv or _StubCV(),  # type: ignore[arg-type]
    )


def _white_image(w: int = 800, h: int = 600) -> Image.Image:
    return Image.new("RGB", (w, h), color="white")


